<h2 class="welcomeh2">Getting Started with SchoolBoogy Student Edition</h2>
<h3 class="welcomeH3">Quick Start</h3>
<h4 class="welcomeh4">SchoolBoogy is easy to use.</h4>  
<ol>
<li>On the left, you will find a list of your courses. Click on a course to reveal the lessons for each course.  The first button under each lesson is the Course Summary.  Be sure to read it first to get a good idea of what to expect in each course.</li> 
<li>Click on a Lesson Button located on the left.  Clicking a Lesson Button sets the Curricula buttons on the right to that lesson.  Until you click on a different lesson, the Curricula buttons will display the Textbook pages, Videos Tutorials, Chalk Talks, Practice Exercises and Learning More links for that lesson.</li>   
<li>To access the material for a different course, simply click the course name to reveal lessons for that course.
</ol>
<h3 class="welcomeH3">Understanding is Different From Knowledge</h3>
<p>Most people confuse understanding with having acquired knowledge. Understanding simply means that you understood the concept being presented to you at the time. Understanding only becomes knowledge with practice and repetition over time. Knowledge means that you have permanently mastered lesson material with accurate recall over time. SchoolBoogy is designed to create knowledge, and provides all the tools you need to master your class material.</p> 
<ul>
<li><a href="">TextBooks</a>: Your digital interactive textbook organized by lesson.</li>
<li><a href="">Video Tutorials</a>: SchoolBoogy Video Tutorials to reinforce learning.</li>
<li><a href="">Chalk Talks</a>: Your teachers can create their own video tutorials for added learning.</li>
<li><a href="">Practice Exercises</a>: These practice exercises will increase your knowledge over time</li>
<li><a href="">Learning More</a>: These are links and other resources to compliment SchoolBoogy material.</li>
<li><a href="">Homework</a>: Find your homework assignments here as your teacher assigns them. </li>
</ul>
<h3 class="welcomeH3">Learning Plans Are the Key To Your Success.</h3>
<p>Learning Plans show you your learning strengths and weakness.  Every time you take a quiz, test or other assessment, SchoolBoogy records your results and shows you the areas you did well on and the areas where you need more study.  Your Learning Plan is more than just a list of strengths and weaknesses.  Your Learning Plan links you to all the material you will need to review to increase your skills on a given lesson.</p>

<h3 class="welcomeH3">Learning Zones Let You Know How You Are Doing</h3>
<p>Learning Zones let you track your personal progress at a glance.  Rather than focus on number grade, Learning Zones allow you to track progress visually.  The object is to move your learning from Red Zone knowledge to Green Zone knowledge.  Learning Zones work hand in hand with your Learning Plan making possible to continuously improve knowledge and achieve better results.
